package JavaClasses;

public class DataTypesPrimitive {
    public static void main(String[] args) {
        byte a=10;
        int value=100;
        float Number=12.345f;
        double Value= 100.98;
        long value1=101010101011l;
        short value2=1212;
        boolean Num=false;
        char alphabet='A';
        String s="Hello Every One";

        System.out.println("Byte Value is = " +a);
        System.out.println("Int Value is = " +value);
        System.out.println("Float Value is = " +Number);
        System.out.println("Double Value is = " +Value);
        System.out.println("Long Value is = " +value1);
        System.out.println("Short Value is = " +value2);
        System.out.println("Boolean Value is = " +Num);
        System.out.println("Char Value is = " +alphabet);
        System.out.println("String Value is = " +s);
    }
}
