package JavaClasses.class2;

public class ArithmeticOperators {
    public static void main(String[] args) {

        //Arithmetic Operators: * / % + -
        int a=5;
        int b=10;
        int result=a+b;
        int result1=a-b;
        int result2=a*b;
        int result3=a/b;
        int result4=a%b;

        System.out.println("The Sum Value is: " +result);
        System.out.println("The Subtraction Value is: " +result1);
        System.out.println("The Multiplication Value is: " +result2);
        System.out.println("The Division Value is: " +result3);
        System.out.println("The  Modulue/RemainderValue is: " +result4);



    }
}
