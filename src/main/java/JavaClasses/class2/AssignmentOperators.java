package JavaClasses.class2;

public class AssignmentOperators {
    public static void main(String[] args) {

        //Assignment Operator: = += -= *= /= %=
        int a = 26, b = 5;

        System.out.println("a = " + a);
        System.out.println("b = " + b);

        a += b;
        System.out.println("a = " + a);

        a -=b;
        System.out.println("a = " + a);

        a *= b;
        System.out.println("a = " + a);

        a /= b;
        System.out.println("a = " + a);

        a %= b;
        System.out.println("a = " + a);
    }
}
