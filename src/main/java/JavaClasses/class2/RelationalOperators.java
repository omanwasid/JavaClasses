package JavaClasses.class2;

public class RelationalOperators {
    public static void main(String[] args) {

        //Relational Operators: < > <= >= == !=
        int a=10;
        int b=10;
        System.out.println(" a==b :" +(a==b));
        System.out.println(" a!=b :" +(a!=b));
        System.out.println(" a>b :" +(a>b));
        System.out.println(" a<b :" +(a<b));
        System.out.println(" a<=b :" +(a<=b));
        System.out.println(" a>=b :" +(a>=b));
    }
}
